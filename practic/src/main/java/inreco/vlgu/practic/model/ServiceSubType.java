package inreco.vlgu.practic.model;

import javax.persistence.*;

@Entity
@Table(name = "service_sub_type",schema = "public")
public class ServiceSubType {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Column(name = "id")
    private int id;

    @Column(name = "name")
    private String name;


    public ServiceSubType() {
    }

    public ServiceSubType(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}

